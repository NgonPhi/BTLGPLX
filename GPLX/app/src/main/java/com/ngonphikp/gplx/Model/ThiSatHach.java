package com.ngonphikp.gplx.Model;

public class ThiSatHach {
    private int current;
    private int size;

    public ThiSatHach(int current, int size) {
        this.current = current;
        this.size = size;
    }

    public int getCurrent() {
        return current;
    }

    public void setCurrent(int current) {
        this.current = current;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

}
